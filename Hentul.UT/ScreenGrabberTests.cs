        [Test, Ignore("this is only for physical cursor movement!")]
        public void TestGetNextCursorPosition()
        {
            sg = new ScreenGrabber(count);

            int iterations = 21;

            int range = count;

            POINT p;

            int i = 1;
            p.X = sg.LeftUpper.Item1;
            p.Y = sg.LeftUpper.Item2;
            

            while (sg.CurrentDirection == "RIGHT")
            {
                p = sg.GetNextCursorPosition(true, p.X, p.Y);

                if (sg.CurrentDirection == "DOWN")
                    break;

                if(p.X < sg.LeftUpper.Item1-1 || p.Y < sg.LeftUpper.Item2-1 || p.X > sg.RightBottom.Item1 + range + 1  || p.Y > sg.RightBottom.Item2 + range + 1)
                {
                    Assert.Fail();
                }

                if (p.X != sg.LeftUpper.Item1 + (i * 25))
                {
                    Assert.Fail();
                }
                i++;
            }

            i = 1;

            while (sg.CurrentDirection == "DOWN")
            {
                p = sg.GetNextCursorPosition(true, p.X, p.Y);

                if (sg.CurrentDirection == "LEFT")
                    break;

                if (p.Y != sg.LeftUpper.Item2 + (i * 25))
                {
                    Assert.Fail();
                }

                if (p.X < sg.LeftUpper.Item1 - 1 || p.Y < sg.LeftUpper.Item2 - 1 || p.X > sg.RightBottom.Item1 + range + 1 || p.Y > sg.RightBottom.Item2 +  range + 1)
                {
                    Assert.Fail();
                }
                i++;
            }

            i = 1;
            while (sg.CurrentDirection == "LEFT")
            {
                p = sg.GetNextCursorPosition(true, p.X, p.Y);

                if (sg.CurrentDirection == "UP")
                    break;

                if (p.X != sg.RightBottom.Item1 - (i * 25))
                {
                    Assert.Fail();
                }

                if (p.X < sg.LeftUpper.Item1 - range - 1 || p.Y < sg.LeftUpper.Item2 - range - 1 || p.X > sg.RightBottom.Item1  || p.Y > sg.RightBottom.Item2)
                {
                    Assert.Fail();
                }

                i++;
            }

            i = 1;

            while (sg.CurrentDirection == "UP")
            {
                p = sg.GetNextCursorPosition(true, p.X, p.Y);

                if (sg.CurrentDirection == "RIGHT")
                {
                    p = sg.GetNextCursorPosition(true, p.X, p.Y);
                    break;
                }

                if (p.Y != sg.LeftBottom.Item2 - (i * 25))
                {
                    Assert.Fail();
                }

                if (p.X < sg.LeftUpper.Item1 - range - 1 || p.Y < sg.LeftUpper.Item2 || p.X > sg.RightBottom.Item1 || p.Y > sg.RightBottom.Item2 )
                {
                    Assert.Fail();
                }
                i++;
            }

            if (p.X < sg.LeftUpper.Item1 || p.Y < sg.LeftUpper.Item2 || p.X > sg.RightBottom.Item1 || p.Y > sg.RightBottom.Item2)
            {
                Assert.Fail();
            }

            Assert.AreEqual(sg.LeftUpper.Item1 + 2 * range, p.X);
            Assert.AreEqual(sg.LeftUpper.Item2 + range, p.Y);
        }

